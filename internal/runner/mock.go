package runner

import (
	"fmt"
	"os"
	"path/filepath"
	"time"

	"github.com/xrf9268-hue/aiops-platform/internal/task"
)

func RunMock(workdir string, t task.Task) error {
	dir := filepath.Join(workdir, ".aiops")
	if err := os.MkdirAll(dir, 0o755); err != nil { return err }
	content := fmt.Sprintf(`# AI Ops Mock Run

Task: %s
Title: %s
Actor: %s
Model: %s
Completed at: %s

This file is generated by the Day 1 mock runner. Replace model=mock with codex or claude after the pipeline is proven.
`, t.ID, t.Title, t.Actor, t.Model, time.Now().Format(time.RFC3339))
	return os.WriteFile(filepath.Join(dir, t.ID+".md"), []byte(content), 0o644)
}
